float calcul_air()
{
	PERIOD_PTS filtered_period[2][NBR_SAMPLES];
	uint8_t p_cnt = 0, m_cnt = 0;
	float aire_p = 0;
	float aire_m = 0;
	float p = 0;

	for(uint16_t j = 0 ; j < NBR_SAMPLES ; j++)
	{
		filtered_period[0][j].pts = 0;
		filtered_period[1][j].pts = 0;

		filtered_period[0][j].index = 0;
		filtered_period[1][j].index = 0;
	}

	for(int i = 0; i < NBR_SAMPLES; i++)
	{
		p = (float)(period_points[i]/ F_CLK);
		p= p - (1/F_THRESHOLD);
//		filtered_period[i][0].pts = p;
//		filtered_period[i][0].index = i;

		if(p >= 0)
		{
			filtered_period[0][p_cnt].pts = p;
			filtered_period[0][p_cnt].index = i;
			p_cnt++;

			if(p_cnt != 1)
			{
				aire_p += (filtered_period[0][p_cnt].pts - filtered_period[0][p_cnt - 1].pts) * (filtered_period[0][p_cnt].index - filtered_period[0][p_cnt - 1].index) ;
			}
		}
		else
		{
			filtered_period[1][m_cnt].pts = p;
			filtered_period[1][m_cnt].index = i;
			m_cnt++;

			if(m_cnt != 1)
			{
				aire_m += (filtered_period[1][m_cnt].pts - filtered_period[1][m_cnt - 1].pts) * (filtered_period[1][m_cnt].index - filtered_period[1][m_cnt - 1].index) ;
			}
		}

	}

	return aire_p - aire_m;

}

